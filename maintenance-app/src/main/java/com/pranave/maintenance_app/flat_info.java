package com.pranave.maintenance_app;

public class flat_info {
	private int flat_no;
	private int bedrooms;
	private long flat_cost;
	private long maintenance;
	public int getFlat_no() {
		return flat_no;
	}
	public void setFlat_no(int flat_no) {
		this.flat_no = flat_no;
	}
	public int getBedrooms() {
		return bedrooms;
	}
	public void setBedrooms(int bedrooms) {
		this.bedrooms = bedrooms;
	}
	public long getFlat_cost() {
		return flat_cost;
	}
	public void setFlat_cost(long flat_cost) {
		this.flat_cost = flat_cost;
	}
	public long getMaintenance() {
		return maintenance;
	}
	public void setMaintenance(long maintenance) {
		this.maintenance = maintenance;
	}
	

}
